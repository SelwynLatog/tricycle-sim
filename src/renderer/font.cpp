// screen-space shader
static const char* FONT_VERT = R"(
#version 330 core
layout(location = 0) in vec2 a_pos;
layout(location = 1) in vec2 a_uv;

uniform vec2 u_screen;  // window dimensions in pixels

out vec2 v_uv;

void main(){
    // convert pixel coords to NDC [-1, 1]
    vec2 ndc = (a_pos / u_screen) * 2.0 - 1.0;
    ndc.y = -ndc.y;  // flip Y: pixel 0 = top, NDC 1 = top
    gl_Position = vec4(ndc, 0.0, 1.0);
    v_uv = a_uv;
}
)";

static const char* FONT_FRAG = R"(
#version 330 core
in  vec2 v_uv;
out vec4 frag_color;

uniform sampler2D u_atlas;
uniform vec3      u_color;

void main(){
    float a = texture(u_atlas, v_uv).r;
    if (a < 0.5) discard;  // transparent pixel
    frag_color = vec4(u_color, 1.0);
}
)";

// font_init
void font_init(Font& f, int window_width, int window_height){
    f.win_w = window_width;
    f.win_h = window_height;

    // build atlas texture: 16 glyphs wide x 6 rows tall, each 8x8 px
    // total: 128 x 48 pixels, single channel (R8)
    const int COLS= 16;
    const int ROWS= 6;
    const int GLYPH_W= 8;
    const int GLYPH_H= 8;
    const int ATLAS_W= COLS * GLYPH_W;   // 128
    const int ATLAS_H= ROWS * GLYPH_H;   // 48

    unsigned char atlas[ATLAS_W * ATLAS_H] = {};

    for (int ci = 0; ci < 95; ci++){
        int col = ci % COLS;
        int row = ci / COLS;
        for (int gy = 0; gy < GLYPH_H; gy++){
            unsigned char byte = FONT_BITMAP[ci][gy];
            for (int gx = 0; gx < GLYPH_W; gx++){
                // MSB = leftmost pixel
                bool lit = (byte >> (7 - gx)) & 1;
                int px = col * GLYPH_W + gx;
                int py = row * GLYPH_H + gy;
                atlas[py * ATLAS_W + px] = lit ? 255 : 0;
            }
        }
    }

    glGenTextures(1, &f.texture);
    glBindTexture(GL_TEXTURE_2D, f.texture);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_R8, ATLAS_W, ATLAS_H,
                 0, GL_RED, GL_UNSIGNED_BYTE, atlas);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glBindTexture(GL_TEXTURE_2D, 0);

    // compile shader
    auto compile = [](GLenum type, const char* src) -> GLuint {
        GLuint s = glCreateShader(type);
        glShaderSource(s, 1, &src, nullptr);
        glCompileShader(s);
        int ok; char log[512];
        glGetShaderiv(s, GL_COMPILE_STATUS, &ok);
        if (!ok){ glGetShaderInfoLog(s, 512, nullptr, log);
                  printf("[font] shader error: %s\n", log); }
        return s;
    };

    GLuint vert = compile(GL_VERTEX_SHADER,   FONT_VERT);
    GLuint frag = compile(GL_FRAGMENT_SHADER, FONT_FRAG);
    f.shader = glCreateProgram();
    glAttachShader(f.shader, vert);
    glAttachShader(f.shader, frag);
    glLinkProgram(f.shader);
    glDeleteShader(vert);
    glDeleteShader(frag);

    // VAO/VBO: 6 verts per glyph (2 tris), each vert = vec2 pos + vec2 uv
    glGenVertexArrays(1, &f.vao);
    glGenBuffers(1, &f.vbo);
    glBindVertexArray(f.vao);
    glBindBuffer(GL_ARRAY_BUFFER, f.vbo);
    // allocate for max 256 chars per draw call
    glBufferData(GL_ARRAY_BUFFER, 256 * 6 * 4 * sizeof(float), nullptr, GL_DYNAMIC_DRAW);
    // pos
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4*sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    // uv
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4*sizeof(float), (void*)(2*sizeof(float)));
    glEnableVertexAttribArray(1);
    glBindVertexArray(0);
}

// font_draw
void font_draw(const Font& f, const std::string& text, int x, int y,
               int scale, float r, float g, float b){
    const int COLS = 16;
    const int GLYPH_W = 8;
    const int GLYPH_H = 8;
    const int ATLAS_W = 128;
    const int ATLAS_H = 48;

    // build vertex buffer: 6 verts per character (2 triangles)
    // layout: x y u v
    std::vector<float> verts;
    verts.reserve(text.size() * 6 * 4);

    int cursor_x = x;
    int cursor_y = y;

    for (char c : text) {
        if (c == '\n') {
            cursor_x  = x;
            cursor_y += GLYPH_H * scale;
            continue;
        }

        int ci = (int)c - 32;  // bitmap index: space=0, '~'=94
        if (ci < 0 || ci > 94) ci = 0;

        int col = ci % COLS;
        int row = ci / COLS;

        // pixel bounds of this glyph in the atlas
        float u0 = (float)(col * GLYPH_W) / ATLAS_W;
        float u1 = (float)(col * GLYPH_W + GLYPH_W) / ATLAS_W;
        float v0 = (float)(row * GLYPH_H) / ATLAS_H;
        float v1 = (float)(row * GLYPH_H + GLYPH_H) / ATLAS_H;

        // screen pixel bounds of this glyph
        float px0 = (float)cursor_x;
        float px1 = (float)(cursor_x + GLYPH_W * scale);
        float py0 = (float)cursor_y;
        float py1 = (float)(cursor_y + GLYPH_H * scale);

        // triangle 1
        verts.insert(verts.end(), {px0,py0,u0,v0});
        verts.insert(verts.end(), {px1,py0,u1,v0});
        verts.insert(verts.end(), {px1,py1,u1,v1});
        // triangle 2
        verts.insert(verts.end(), {px0,py0,u0,v0});
        verts.insert(verts.end(), {px1,py1,u1,v1});
        verts.insert(verts.end(), {px0,py1,u0,v1});

        cursor_x += GLYPH_W * scale;
    }

    if (verts.empty()) return;

    // upload and draw
    glUseProgram(f.shader);

    // set uniforms
    glUniform2f(glGetUniformLocation(f.shader, "u_screen"),
                (float)f.win_w, (float)f.win_h);
    glUniform3f(glGetUniformLocation(f.shader, "u_color"), r, g, b);
    glUniform1i(glGetUniformLocation(f.shader, "u_atlas"), 0);

    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, f.texture);

    glBindVertexArray(f.vao);
    glBindBuffer(GL_ARRAY_BUFFER, f.vbo);
    glBufferSubData(GL_ARRAY_BUFFER, 0,
                    verts.size() * sizeof(float), verts.data());

    // disable depth test so HUD always draws on top
    glDisable(GL_DEPTH_TEST);
    glDrawArrays(GL_TRIANGLES, 0, (int)verts.size() / 4);
    glEnable(GL_DEPTH_TEST);

    glBindVertexArray(0);
    glBindTexture(GL_TEXTURE_2D, 0);
}

// font_destroy
void font_destroy(Font& f){
    glDeleteTextures(1, &f.texture);
    glDeleteVertexArrays(1, &f.vao);
    glDeleteBuffers(1, &f.vbo);
    glDeleteProgram(f.shader);
    f.texture = f.vao = f.vbo = f.shader = 0;
}